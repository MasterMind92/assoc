                <h2>Le Président</h2>
            <p>Monsieur <strong>Jacques Maxime TRAORE</strong> est Macro-économiste et Spécialiste en Finances Publiques a travaillé pendant plus de dix années dans des institutions internationales en tant que Chef de projets. </p>
            <p>Il a pu durant sa carrière parcourir de nombreux pays africains et s'enquérir des réels besoins des populations qui bien souvent malheureusement ne s'accordaient pas avec les projets pensés et établis avant d'être soumis ensuite aux populations. Ces projets, bien que représentant une aide pour ces bénéficiaires dans une certaine mesure, finissent par mourir du fait de leur inéquation ou incomplétude vis a vis des besoins des bénéficiaires.</p>
<p>C'est de ce constat qu'est né la volonté de créer l'<strong>Association ADSE</strong> avec une approche totalement différente de celle de ces institutions.</p>
